	<p>Дарон Малакян - Дарон Вартанович Малакян родился 18 июля 1975 года в Голливуде.<br><br>
		Он единственный ребёнок Вартана и Зепюр Малакян, иммигрантов из Ирана и Ирака.
		Вартан Малакян — художник, танцор и хореограф. Зепур Малакян — скульптор, ранее преподававшая в колледже.
		В раннем детстве Дарон начал слушать хеви-метал. Его троюродный брат играл ему песни Kiss, когда Дарону было 3 года.
		Малакян начал слушать Van Halen, Iron Maiden, Judas Priest, Motörhead и Оззи Осборна. Он всегда хотел играть на ударных,
		но родители купили ему гитару, потому что барабаны нельзя выключить. В 11 лет он первый раз взял в руки гитару<br>
		В своем интервью он говорит: <br><br>«В первый год обучения, я совершенствовал свой слух. Вроде бы это у меня получалось неплохо.
		После нескольких лет я заработал репутацию начинающего гитариста в старших классах школы. А к 16-17 годам я осознал,
	    что это хороший инструмент для сочинения музыки, и самое главное — что мне это нравится. Но на роль мистера „Гитарный виртуоз“ 
		я конечно не претендовал»<br><br> В подростковом возрасте Малакян слушал такие метал группы, как Slayer, Venom, Metallica,
		Pantera и Sepultura. Затем Малакян начал слушать The Beatles, и цитаты Джона Леннона повлияли на него, как на композитора.
		Так же, он говорит, на него повлияли такие британские группы, как The Kinks и The Who. Существенное влияние на него
		оказали фолк-рок трио Peter, Paul and Mary и пионер панк-рока Игги Поп. <br>Дарон ходил в армянскую школу Роуз и Алекса Пилибос
		вместе с его будущими коллегами по группе Шаво Одаджяном и Энди Хачатуряном. Вокалист System of a Down Серж Танкян также посещал 
		эту школу, но за несколько лет до Малакяна и остальных. В подростковом возрасте Малакян посещал Высшую школу Глендейла.<br><br>

		Малакян встретил Танкяна в 1993, когда их группы репетировали в одной студии. Танкян играл на клавишах в одной группе, а Малакян был вокалистом
		и гитаристом в другой. Вместе с басистом Дейвом Хакопяном и ударником Доминго Лареньо они основали группу Soil. Затем Шаво Одаджян стал их менеджером
		и ритм-гитаристом. Soil распался, и Малакян, Танкян и Одаджян (который переключился на бас) создали новую группу System of a Down. Название группы происходит
		от стихотворения Дарона. Стихотворение называлось Victims of a Down, но Одаджян предложил заменить «victims» на «system». Затем к ним присоединился ударник
		Энди Хачатурян, которого в 1997 заменил Джон Долмаян.<br><br>

		Малакян продюсировал альбомы System of a Down вместе с Риком Рубином , также, как и альбомы групп The Ambulance и Bad Acid Trip В 2003 году Дарон организовал
		собственный звукозаписывающий лейбл EatUrMusic, и первым релизом студии стал альбом группы Amen. На данный момент лейбл закрыт.</p>
