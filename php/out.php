	<p>Серж Танкиян - Серж родился в Ливане 21 августа 1967 года и провёл там детство. 
		В возрасте 8 лет вместе с родителями и младшим братом Севаком он переехал в Лос-Анджелес,
		где начал заниматься музыкой в армянской школе «Rose & Alex Philibosi», одновременно изучая маркетинг.<br>

		В Лос-Анджелесе он примкнул к большому армянскому сообществу. У него есть собственный лейбл Serjical Strike Records, а кроме того он опубликовал свою книгу 
		стихов «Cool Gardens». Серж оплачивал первые издания книги за счёт своей небольшой фирмы по производству программного обеспечения «Ultimate Solutions».<br>

		В 1994 Танкян познакомился с Дароном Малакяном, вместе они организовали группу «System of a Down», в состав которой вошёл также барабанщик Энди (Андраник) Хачатурян
		(потом из-за разногласий он покинул группу). Впоследствии Танкян и Малакян знакомятся с Шаво Одаджяном, который на тот момент времени работал в банке и учился в той
		же музыкальной школе, что и Серж. Временно он стал их менеджером, но позже бросил работу и стал бас-гитаристом. Таким образом произошло создание группы «System of a Down»,
		в состав которой вошёл также барабанщик Джон Долмаян.<br>
	
		Сольный проект Сержа Танкяна — его дебютный альбом «Elect The Dead», состоящий как из старых песен, по тем или иным причинам не вошедших в репертуар System Of A Down,
		так и совершенно нового материала, вышел 23 октября 2007 года. На концертный тур в поддержку своего альбома Танкян собрал состав, который назывался Serj Tankian And 
		The F.C.C. Тур в поддержку альбома начался 12 октября 2007 года в Чикаго.<br>

		Серж Танкян принимал участие в записи альбома «Profanation» группы Билла Ласвелла «Praxis», вышедшего в 2008 году.<br>

		В начале 2010 года Серж выпускает первую песню под симфонический оркестр. Дебютом стала песня Empty Walls, являющаяся флагманом в его 
		альбоме Elect the Dead. 9 марта 2010 года Серж выпускает альбом с симфоническим оркестром<br>

		В конце февраля 2011 года Серж выпустил мюзикл про Прометея [1]. 21 сентября 2010 вышел его новый альбом Imperfect Harmonies. Музыкант
		 позже выпустил EP «Imperfect Remixes»(2011 год). Это коллекция ремиксов на песни с альбома «Imperfect Harmonies».<br>

		12 августа 2011 года Танкян, перед его очередным концертом в Ереване, был награждён медалью премьер-министра Армении за его вклад в 
		признание Геноцида армян и за продвижения армянской музыки.[2][3]<br>

		9 июня 2012 года в Эджмиацине состоялась свадьба Сержа Танкяна с Анджелой Мадатян.<br>

		10 июля 2012 года Серж выпустил свой новый альбом "Harakiri", который он сам описал в интервью как "обычный рок-альбом,
		 который сильно схож с творчеством System of a Down"<br>

		В начале 2013 года, Танкян объявил о своем сотрудничестве с хип-хоп исполнителем Tech N9ne, и намерении принять участие в записи
		 его альбома "Something Else".[2]<br>
		Интересные факты<br>

		Любимые группы Сержа: «Black Sabbath», «Korn», «Metallica» и «Slayer».<br>
		Компания Taylor выпустила именную гитару Serj Tankian Signature Model T5 (STSM-T5) с эмблемой его талисмана, который он нашел во время турне с System of a Down по Европе.<br>
		В одном из интервью для британской газеты «The Times» Серж признался, что любимый жанр кинематографа — фильмы ужасов. А самым любимым фильмом является величайший, по его 
		мнению, фильм ужасов всех времён — шедевр американского кинематографа «Омен.»<br>
		26 ноября 2009 г. в прямом эфире армянского телевидения Серж со своим отцом Хачатуром Танкяном исполнили на армянском языке песню Алексея Экимяна[4].<br>
		Серж Танкян — вегетарианец[5][6][7].<br> В интервью с PETA он сказал, что изменение образа его жизни было связано с «поеданием разнообразного съедобного дерьма
		 во время гастролей», а также он отметил, что это было «несколько инстинктивно». Кроме того, он также чувствует потребность в уважении к «матери-земле»[8].<br>
		В июле 2009 года Танкян подписал петицию РЕТА против методов забоя кур в бойнях KFC.[9][10].<br>
