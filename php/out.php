	<p>Ранние годы

		В юности Шаво занимался скейтбордингом и слушал панк-рок и хэви-метал; среди его любимых исполнителей значатся Pink Angel, Dead Kennedys, Kiss и Black Sabbath.<br>

		Совсем молодым Одаджян переехал в Лос-Анджелес, Калифорния, где основную роль в его воспитании играла его бабушка, после смерти которой (по словам самого Шаво)
		он утратил веру, и перестал молиться перед сном, как раньше. Он посещал начальную школу имени Алекса Пилибоса в Лос-Анджелесе, армянскую приходскую школу, 
		вместе с будущими коллегами по группе Дароном Малакяном и Сержом Танкяном, хотя они не были знакомы во время обучения из-за разницы в возрасте.<br>
		Карьера<br>

		Работая в банке, Шаво начал руководить группой SOAD с Малакяном и Танкяном в 1994 году после того, как повстречал их в звукозаписывающей студии. В 1995-ом 
		Шаво стал постоянным басистом группы. Позже они переименовали себя в «System of a Down», после того как Дарон Малакян написал стихотворение «Victims of The Down».<br>

		Одаджян — популярный диджей в Лос-Анджелесе, он принимал участие в таких событиях, как «Rock/DJ Explosion» 2 марта 2001 в The Roxy, Голливуд, и работал с 
		Танкяном над SerArt, сторонним проектом певца, записанным вместе с армянским музыкантом Арто Тунджбояджяном. Шаво известен под именем «DJ Tactic». Он также 
		является исполнительным продюсером группы Onesidezero.<br>
		Кино<br>

		Определяя себя как «очень визуализирующую персону», он был режиссёром многих клипов System of a Down («Aerials», «Toxicity», «Question!», и «Hypnotize») и 
		Taproot, создавая сюрреалистичную атмосферу и используя инновационные методы киносъёмки. В 1993 году снялся в клипе «Big Gun» группы AC/DC, где оказался в 
		толпе рядом с Арнольдом Шварценеггером.<br>

		Снялся в фильме «Образцовый самец».<br>
		Одаджян — музыкант<br>
		Шаво первоначально был гитаристом, и утверждает, что на гитаре он играет лучше, чем на басу. Фактически, некоторое время он был ритм-гитаристом в Soil. 
		Кроме игры на басу, Шаво записал несколько вокальных партий для песни «Bounce». В «Lost in Hollywood» бэк-вокал ближе к концу песни тоже исполняет Шаво.</p>
